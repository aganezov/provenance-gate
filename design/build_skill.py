def _skill_cockpit_html() -> str:
    """The in-CS cockpit template, generated from ui/cockpit.html: inlined vendor -> __BUNDLE_SRC__
    marker, plus the PG:SKILL-DATA offline data override and a no-bundle fallback. Same file the
    standalone cockpit ships (toggle, frames, inspector, rail, trace, isolate); render_cockpit fills it."""
    design_dir = str(ROOT / "design")
    if design_dir not in sys.path:   # don't accumulate copies across a watch()-loop's rebuilds
        sys.path.insert(0, design_dir)
    import vendor_libs
    html = vendor_libs.strip_vendor((ROOT / "ui" / "cockpit.html").read_text())
    if "</head>" not in html:
        sys.exit("build_skill: no </head> in ui/cockpit.html — cannot place the bundle <script src>")
    html = html.replace("</head>", '<script src="__BUNDLE_SRC__"></script>\n</head>', 1)
    if "// ===== /PG:LOG =====" not in html:
        sys.exit("build_skill: no PG:LOG anchor in ui/cockpit.html — where does the data override go?")
    html = html.replace("// ===== /PG:LOG =====", "// ===== /PG:LOG =====" + SKILL_DATA_BLOCK, 1)
    if "</body>" not in html:
        sys.exit("build_skill: no </body> in ui/cockpit.html — cannot place the fallback")
    return html.replace("</body>", SKILL_FALLBACK_BLOCK + "</body>", 1)

# The kernel header is just the module docstring + the hard constraints. The inlined modules KEEP
# their own stdlib/typing imports (they become function-level inside pg_impl), so adding a new import
# to a core module needs no change here and can't silently vanish from the kernel.
STDLIB_HEADER = (
    '"""__SKILL_NAME__ CS skill kernel — AUTO-GENERATED by design/build_skill.py. DO NOT EDIT HERE."""\n'
    "# No 'from __future__' (the CS plugin loader relocates the source, breaking its must-be-first\n"
    "# rule) and no slots=/PEP-604 unions — the control-plane repl kernel (host.query) is Python 3.9.\n"
)

# Drop ONLY these when inlining: __future__ (can't be relocated by the loader) and relative imports
# (their targets are inlined in-file). Every other import is kept, nested inside pg_impl.
_DROP_PREFIXES = ("from __future__", "from .")


def _strip_imports(src: str) -> str:
    keep = [ln for ln in src.splitlines() if not any(ln.lstrip().startswith(p) for p in _DROP_PREFIXES)]
    return "\n".join(keep).strip("\n")


def _core_bodies() -> str:
    parts = []
    for mod in MODULES:
        body = _strip_imports((SRC / mod).read_text())
        parts.append(f"# ================ inlined: provenance_gate/{mod} ================\n{body}")
    return "\n\n\n".join(parts)


def _kernel_source() -> str:
    # The CS sidecar gate rejects a kernel.py whose TOP LEVEL has _-prefixed names, classes, or
    # try/except — only functions, imports, and literal-constant assignments pass. So nest the ENTIRE
    # inlined core (dataclasses + _-helpers) inside one top-level pg_impl(host); the module top level
    # is then just imports + three functions. (The skill dir is also not on sys.path, so inlining, not
    # sibling imports, is required regardless.) Proven shape: merge-lineage-audit nests everything too.
    import textwrap
    nested = _core_bodies() + "\n\n\n" + KERNEL_IMPL
    return (
        STDLIB_HEADER
        + "\n\ndef pg_impl(host):\n"
        + textwrap.indent(nested, "    ")
        + '\n    return {"audit_project": audit_project, "audit_input_lineage": audit_input_lineage,'
        + '\n            "render_cockpit": render_cockpit}\n'
        + WRAPPERS
    ).replace(", slots=True", "").replace("__SKILL_NAME__", SKILL_NAME)


KERNEL_IMPL = '''

# ================ in-CS kernel (nested inside pg_impl(host)) ================
# host is pg_impl's arg (the CS-injected object). Reads via host.query(scope="project"), derives with
# the inlined core, judges with the inlined audit. Everything is nested so the module top level stays
# gate-clean; the top-level wrappers below call pg_impl(host)[...].


class _HostReader:
    """Adapt the injected ``host`` to the CsHost the reader wants: a project-scoped query returning
    list[dict]. Normalises host.query's shape ({rows,columns} tuple-rows OR dict-rows)."""

    def query(self, sql):
        result = host.query(sql, scope="project")  # noqa: F821  (host injected by CS)
        if isinstance(result, dict) and "rows" in result:
            rows = list(result["rows"])
            if not rows or isinstance(rows[0], dict):
                return rows
            cols = result.get("columns") or result.get("column_names") or result.get("fields") or []
            names = [c if isinstance(c, str) else (c.get("name") or c.get("id") or c.get("label") or str(c)) for c in cols]
            return [dict(zip(names, r)) for r in rows] if names else rows
        return [dict(r) for r in result]


def _reader():
    return HostQueryReader(_HostReader(), scope_by_host=True)


def _current_project():
    # ORDER BY so the pick is deterministic if host.query ever hands back >1 project (name is shown in
    # the cockpit header now); most-recently-updated, matching adapters/external/substrate.list_projects.
    rows = _HostReader().query("SELECT id, name FROM projects ORDER BY updated_at DESC LIMIT 1")
    if rows:
        return {"id": rows[0]["id"], "name": rows[0].get("name") or rows[0]["id"]}
    return {"id": "current", "name": "current"}


def _current_project_id():
    return _current_project()["id"]


def _issue(vi):
    return {"artifact": vi.artifact, "versions": list(vi.versions), "current": vi.current}


def audit_project():
    """Trust verdict for every computation in the current CS project: which cells consume a stale
    input (stale_input) or merge two live versions of one artifact (version_mix). Reuses core.audit."""
    g = _reader().read_project_graph(_current_project_id())
    verdicts = audit_graph(g)
    label = {n.id: n.label for n in g.nodes}
    flagged = sorted(
        (
            {"cell": label.get(nid, nid), "verdict": v.level,
             "stale": [_issue(s) for s in v.stale], "mixed": [_issue(m) for m in v.mixed]}
            for nid, v in verdicts.items() if v.level != CLEAN
        ),
        key=lambda f: f["cell"],
    )
    return {"project": g.cs_project_id, "cells": len(g.nodes),
            "clean": sum(1 for v in verdicts.values() if v.level == CLEAN), "flagged": flagged}


def audit_input_lineage(input_files, planned_output=None):
    """Pre-write check: given input filenames, the verdict over their latest versions' lineage
    (stale source / version mix) before you write ``planned_output``. Backed by core.audit —
    converges with the merge-lineage-audit helper on the tested engine."""
    if isinstance(input_files, str):
        input_files = [input_files]
    g = _reader().read_project_graph(_current_project_id())
    latest = {a.filename: a.artifact_version_id for n in g.nodes for a in n.output_surface if a.is_latest}
    vids, missing = [], []
    for f in input_files:
        if f in latest:
            vids.append(latest[f])
        else:
            missing.append(f)
    if not vids:
        return {"planned_output": planned_output, "missing_inputs": missing,
                "verdict": "LINEAGE_MISSING" if missing else "NOT_APPLICABLE"}
    v = audit_inputs(g, vids)
    return {"planned_output": planned_output, "missing_inputs": missing, "verdict": v.level,
            "stale": [_issue(s) for s in v.stale], "mixed": [_issue(m) for m in v.mixed]}


def render_cockpit():
    """Write cockpit.html: the full interactive cockpit for the current CS project — the same file the
    standalone gate serves (inspector, rail, trace, isolate, flow toggle, framed DAG), with the project
    graph baked in and the cytoscape bundle referenced by its ARTIFACT MARKER ({{artifact:VID}} -> the
    served URL, the only <script src> form CS's HTTP viewer resolves). The bundle is a shared artifact
    saved once, so cockpit.html stays KB. First call (bundle not yet saved) hands the bundle to the
    agent to save_artifacts once; then this and later renders return cockpit.html to save_artifacts."""
    import json
    import re
    proj = _current_project()
    g = _reader().read_project_graph(proj["id"])
    # The bundle must be a SAVED artifact so we can reference it by marker: {{artifact:VID}} is the only
    # <script src> form that resolves in CS's HTTP viewer (a bare filename or disk path 404s). Find its
    # version id; if it isn't saved yet, hand the bundle to the agent to save once, then re-run.
    rows = _HostReader().query(
        "SELECT v.id FROM artifact_versions v JOIN artifacts a ON a.id = v.artifact_id "
        "WHERE a.filename = 'cytoscape-dagre.bundle.min.js' ORDER BY v.version_number DESC")
    if not rows:
        b = host.skills.read("__SKILL_NAME__", "cytoscape-dagre.bundle.min.js")
        with open("cytoscape-dagre.bundle.min.js", "w", encoding="utf-8") as f:
            f.write(b["content"] if isinstance(b, dict) else b)
        return {"project": g.cs_project_id, "nodes": len(g.nodes), "status": "need_bundle_saved",
                "files": ["cytoscape-dagre.bundle.min.js"],
                "next": "save_artifacts(['cytoscape-dagre.bundle.min.js']) once, then call render_cockpit() again"}
    marker = host.artifact_marker(rows[0]["id"])   # "{{artifact:VID}}" -> the bundle's served URL
    t = host.skills.read("__SKILL_NAME__", "cockpit_template.html")
    t = t["content"] if isinstance(t, dict) else t
    # the getGraph wire shape from the ONE shared serializer (core.audit.graph_response, inlined) —
    # identical to what adapters/external/server.py serves, so the two surfaces can't drift.
    resp = graph_response(g)
    # escape EVERY '<' as \\u003c (valid JSON; '<' only appears inside string values). The baked GRAPH is
    # a live JS object literal in an inline <script>; a '</'-only seal misses the <!--<script> double-escape.
    # chr(92) is a backslash, on purpose: this line is inside the KERNEL_IMPL string, so writing the escape
    # literally would collapse to a bare '<' in the generated kernel and make seal a no-op.
    seal = lambda s: s.replace("<", chr(92) + "u003c")
    subs = {
        "__BUNDLE_SRC__": marker,   # {{artifact:VID}} marker; cockpit.html stays KB, not an inline bundle
        "__GRAPH__": seal(json.dumps(resp)),
        "__PROJECT__": seal(json.dumps({"id": proj["id"], "name": proj["name"]})),
    }
    # ONE pass: inserted values are never re-scanned, so a placeholder token appearing inside a
    # baked value can't corrupt another slot.
    html = re.sub("|".join(re.escape(k) for k in subs), lambda m: subs[m.group(0)], t)
    with open("cockpit.html", "w", encoding="utf-8") as f:
        f.write(html)
    return {"project": g.cs_project_id, "nodes": len(g.nodes), "status": "rendered",
            "files": ["cockpit.html"], "next": "call save_artifacts(['cockpit.html']) to publish"}
'''


# gate-clean top level: only these public functions (+ pg_impl + imports) sit at module scope.
WRAPPERS = '''

def audit_project():
    return pg_impl(host)["audit_project"]()


def audit_input_lineage(input_files, planned_output=None):
    return pg_impl(host)["audit_input_lineage"](input_files, planned_output)


def render_cockpit():
    return pg_impl(host)["render_cockpit"]()


def pg_dev_sync():
    """DEV ONLY: resync this skill's files from ~/pg-share (build_skill --share) via host.skills.edit,
    then start a fresh conversation to load them. Strip before distributing."""
    import os
    base = os.path.expanduser("~/pg-share")
    for staged, path in (("__SKILL_NAME__.kernel.py", "kernel.py"),
                         ("__SKILL_NAME__.SKILL.md", "SKILL.md"),
                         ("__SKILL_NAME__.cockpit_template.html", "cockpit_template.html")):
        try:
            with open(base + "/" + staged, encoding="utf-8") as f:
                host.skills.edit("__SKILL_NAME__", path, f.read())
        except Exception as e:
            return "sync failed on " + path + ": " + repr(e)
    return "synced from ~/pg-share — start a fresh conversation to load the new kernel"
'''


SKILL_MD = '''---
name: __SKILL_NAME__
description: Deterministic trust audit over the current Claude Science project's provenance DAG. Call audit_project() for a per-cell verdict (which computations consume a stale input or merge two live versions of one artifact), or audit_input_lineage(files) before writing an output to check its inputs' lineage. Use when reasoning about whether a result is built on current, consistent upstream data.
---

# __SKILL_NAME__

Deterministic, claim-level provenance audit for the current project. Two helpers are auto-loaded
(definition-only); run them in the repl / control-plane surface (where `host.query` lives):

- **`audit_project()`** — a verdict for every computation cell: `stale_input` (consumes a
  non-current version of some artifact) or `version_mix` (its input lineage reconverges on two live
  versions of one artifact — a divergent-branch merge). Returns the flagged cells with the artifacts
  and version numbers in play.
- **`audit_input_lineage(input_files, planned_output=None)`** — before writing an output, check the
  latest versions of the named input files: same `stale_input` / `version_mix` verdict over their
  lineage closure.
- **`render_cockpit()`** — write an interactive `cockpit.html` (project DAG, each cell coloured by its
  verdict: clean / stale input / version mix), referencing the cytoscape bundle by its artifact marker
  so it stays KB. The first call returns the bundle to `save_artifacts` once; then it (and later renders)
  return `cockpit.html` to `save_artifacts`. Follow the returned `next` field.

All are computed from provenance alone (nothing stored) and are deterministic. A linear revision chain
(reading v1 to write v2) is a supersession, not a conflict, and is not flagged.
'''


def build() -> None:
    import shutil
    DIST.mkdir(parents=True, exist_ok=True)
    src = _kernel_source()
    compile(src, "kernel.py", "exec")   # fail LOUDLY here if the inliner produced invalid Python
    (DIST / "kernel.py").write_text(src)
    (DIST / "SKILL.md").write_text(SKILL_MD.replace("__SKILL_NAME__", SKILL_NAME))
    (DIST / "cockpit_template.html").write_text(_skill_cockpit_html())  # generated from ui/cockpit.html
    for name, srcpath in ASSETS.items():
        shutil.copyfile(srcpath, DIST / name)
    kb = (DIST / "kernel.py").stat().st_size
    ck = (DIST / "cockpit_template.html").stat().st_size
    print(f"built {DIST / 'kernel.py'} ({kb} bytes) + SKILL.md + cockpit_template.html "
          f"({ck // 1024} KB) + {len(ASSETS)} assets")


def zip_() -> None:
    z = DIST / f"{SKILL_NAME}.zip"
    with zipfile.ZipFile(z, "w", zipfile.ZIP_DEFLATED) as zf:
        zf.write(DIST / "kernel.py", "kernel.py")
        zf.write(DIST / "SKILL.md", "SKILL.md")
        zf.write(DIST / "cockpit_template.html", "cockpit_template.html")
        for name in ASSETS:
            zf.write(DIST / name, name)
    print(f"packaged {z} ({z.stat().st_size // 1024} KB)  (upload via Skills -> Add skill -> Upload a skill)")


def share() -> None:
    # The dev loop. Uploaded skills are NOT on disk (probe: skill_dir_on_disk = not on disk) and the
    # kernel has no __file__, so there's no symlink/direct-write path. Instead stage the built files in
    # the sandbox-readable ~/pg-share (probe: pg_share_read = ok); in CS, host.skills.edit reads them.
    import shutil
    dst = pathlib.Path.home() / "pg-share"
    dst.mkdir(parents=True, exist_ok=True)
    # include the bundle so a from-scratch draft (host.skills.edit each file) has all four assets
    for f in ("kernel.py", "SKILL.md", "cockpit_template.html", "cytoscape-dagre.bundle.min.js"):
        shutil.copyfile(DIST / f, dst / f"{SKILL_NAME}.{f}")
    print(f"shared -> {dst}/{SKILL_NAME}.*  (in CS: run pg_dev_sync() + start a fresh conversation)")


def watch() -> None:
    import time
    targets = [ROOT / "src", ROOT / "design" / "build_skill.py", ROOT / "design" / "skill_assets",
               ROOT / "ui" / "cockpit.html"]  # the cockpit template is generated from it

    def snap():
        s = {}
        for t in targets:
            if t.is_file():
                s[t] = t.stat().st_mtime
            else:
                for p in list(t.rglob("*.py")) + list(t.rglob("*.html")):
                    s[p] = p.stat().st_mtime
        return s

    print("watching src/ + design/ — rebuild + --share on save (Ctrl-C to stop)")
    last = None
    while True:
        cur = snap()
        if cur != last:
            build()
            share()
            print("  [rebuilt + shared] -> run pg_dev_sync() in CS + start a fresh conversation")
            last = cur
        time.sleep(1.0)


def main(argv: list[str]) -> None:
    global SKILL_NAME
    if "--name" in argv:
        idx = argv.index("--name")
        if idx + 1 >= len(argv):
            sys.exit("--name requires a value")
        SKILL_NAME = argv[idx + 1]
    if "--watch" in argv:
        watch()
        return
    build()
    if "--zip" in argv:
        zip_()
    if "--share" in argv:
        share()


if __name__ == "__main__":
    main(sys.argv[1:])
