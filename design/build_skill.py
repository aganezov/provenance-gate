def _skill_cockpit_html():
    """Generate the in-CS cockpit from ui/cockpit.html, returning (shell, app_js). The shell is tiny:
    DOM + marker refs to the saved-once vendor bundle + app JS + inline PG:SKILL-DATA (the per-render
    baked graph) + a no-bundle fallback. The ~32KB app CSS is folded into app_js (injected as a <style>
    on load), so both the CSS and the ~65KB JS ride ONE saved-once <script src> marker — no <link>, whose
    marker rewrite the CS viewer isn't known to resolve. render_cockpit fills the markers + baked data."""
    import json
    design_dir = str(ROOT / "design")
    if design_dir not in sys.path:   # don't accumulate copies across a watch()-loop's rebuilds
        sys.path.insert(0, design_dir)
    import vendor_libs
    html = vendor_libs.strip_vendor((ROOT / "ui" / "cockpit.html").read_text())
    if "</head>" not in html:
        sys.exit("build_skill: no </head> in ui/cockpit.html — cannot place the vendor <script src>")
    html = html.replace("</head>", '<script src="__BUNDLE_SRC__"></script>\n</head>', 1)
    # pull the app CSS out of the shell; it rides inside app_js (injected as a <style> when app_js loads)
    if "<style>" not in html or "</style>" not in html:
        sys.exit("build_skill: no <style> block in ui/cockpit.html")
    a, b = html.index("<style>"), html.index("</style>") + len("</style>")
    app_css = html[a + len("<style>"):html.index("</style>")]
    html = html[:a] + "<!-- app CSS is injected by cockpit-app.js -->" + html[b:]
    # the app <script> (PG:DATA-IO .. boot) -> a saved-once artifact; inline PG:SKILL-DATA (per-render
    # baked globals) goes BEFORE the marker so PG:DATA-IO's getGraph reads it when the external app boots.
    if "// ===== PG:DATA-IO" not in html:
        sys.exit("build_skill: no PG:DATA-IO in ui/cockpit.html — can't find the app <script>")
    d = html.index("// ===== PG:DATA-IO")
    o = html.rindex("<script>", 0, d)
    c = html.index("</script>", d)
    app_js = ("(function(){var s=document.createElement('style');s.textContent="
              + json.dumps(app_css) + ";document.head.appendChild(s);})();\n"
              + html[o + len("<script>"):c])
    html = (html[:o] + "<script>" + SKILL_DATA_BLOCK + "\n</script>\n"
            + '<script src="__APP_JS_SRC__"></script>' + html[c + len("</script>"):])
    if "</body>" not in html:
        sys.exit("build_skill: no </body> in ui/cockpit.html — cannot place the fallback")
    html = html.replace("</body>", SKILL_FALLBACK_BLOCK + "</body>", 1)
    return html, app_js

# The kernel header is just the module docstring + the hard constraints. The inlined modules KEEP
# their own stdlib/typing imports (they become function-level inside pg_impl), so adding a new import
# to a core module needs no change here and can't silently vanish from the kernel.
STDLIB_HEADER = (
    '"""__SKILL_NAME__ CS skill kernel — AUTO-GENERATED by design/build_skill.py. DO NOT EDIT HERE."""\n'
    "# No 'from __future__' (the CS plugin loader relocates the source, breaking its must-be-first\n"
    "# rule) and no slots=/PEP-604 unions — the control-plane repl kernel (host.query) is Python 3.9.\n"
)

# Drop ONLY these when inlining: __future__ (can't be relocated by the loader) and relative imports
# (their targets are inlined in-file). Every other import is kept, nested inside pg_impl.
_DROP_PREFIXES = ("from __future__", "from .")


def _strip_imports(src: str) -> str:
    keep = [ln for ln in src.splitlines() if not any(ln.lstrip().startswith(p) for p in _DROP_PREFIXES)]
    return "\n".join(keep).strip("\n")


def _core_bodies() -> str:
    parts = []
    for mod in MODULES:
        body = _strip_imports((SRC / mod).read_text())
        parts.append(f"# ================ inlined: provenance_gate/{mod} ================\n{body}")
    return "\n\n\n".join(parts)


def _kernel_source() -> str:
    # The CS sidecar gate rejects a kernel.py whose TOP LEVEL has _-prefixed names, classes, or
    # try/except — only functions, imports, and literal-constant assignments pass. So nest the ENTIRE
    # inlined core (dataclasses + _-helpers) inside one top-level pg_impl(host); the module top level
    # is then just imports + three functions. (The skill dir is also not on sys.path, so inlining, not
    # sibling imports, is required regardless.) Proven shape: merge-lineage-audit nests everything too.
    import textwrap
    nested = _core_bodies() + "\n\n\n" + KERNEL_IMPL
    return (
        STDLIB_HEADER
        + "\n\ndef pg_impl(host):\n"
        + textwrap.indent(nested, "    ")
        + '\n    return {"audit_project": audit_project, "audit_input_lineage": audit_input_lineage,'
        + '\n            "render_cockpit": render_cockpit, "review_subgraph": review_subgraph,'
        + '\n            "review_chat": review_chat, "review_selection": review_selection}\n'
        + WRAPPERS
    ).replace(", slots=True", "").replace("__SKILL_NAME__", SKILL_NAME)


KERNEL_IMPL = '''

# ================ in-CS kernel (nested inside pg_impl(host)) ================
# host is pg_impl's arg (the CS-injected object). Reads via host.query(scope="project"), derives with
# the inlined core, judges with the inlined audit. Everything is nested so the module top level stays
# gate-clean; the top-level wrappers below call pg_impl(host)[...].


class _HostReader:
    """Adapt the injected ``host`` to the CsHost the reader wants: a project-scoped query returning
    list[dict]. Normalises host.query's shape ({rows,columns} tuple-rows OR dict-rows)."""

    def query(self, sql):
        result = host.query(sql, scope="project")  # noqa: F821  (host injected by CS)
        if isinstance(result, dict) and "rows" in result:
            rows = list(result["rows"])
            if not rows or isinstance(rows[0], dict):
                return rows
            cols = result.get("columns") or result.get("column_names") or result.get("fields") or []
            names = [c if isinstance(c, str) else (c.get("name") or c.get("id") or c.get("label") or str(c)) for c in cols]
            return [dict(zip(names, r)) for r in rows] if names else rows
        return [dict(r) for r in result]


def _reader():
    return HostQueryReader(_HostReader(), scope_by_host=True)


def _current_frame():
    """The current conversation, from the CS repl kernel's CWD: its basename IS the current frame id;
    resolve it to (root_frame_id, project_id). root_frame_id is the whole conversation — a delegated
    sub-agent frame still maps to its chat's root — and project_id is a DETERMINISTIC current project.
    Returns (None, None) when it can't resolve (not a repl CWD, an unknown id, or a legacy schema
    without these columns), so callers fall back to the heuristic project or report no current chat."""
    import os
    try:
        fid = os.path.basename(os.getcwd())   # getcwd inside the guard: a deleted CWD raises OSError
        if not fid:
            return None, None
        rows = _HostReader().query(
            "SELECT root_frame_id, project_id FROM frames WHERE id = '" + _esc(fid) + "'")
    except Exception:
        return None, None
    if rows:
        return rows[0].get("root_frame_id") or fid, rows[0].get("project_id")
    return None, None


def _project_named(pid):
    # {id, name} for a known project id (name looked up) — used once _current_frame hands us the id.
    rows = _HostReader().query("SELECT name FROM projects WHERE id = '" + _esc(pid) + "'")
    return {"id": pid, "name": (rows[0].get("name") if rows else None) or pid}


def _recency_project():
    # Fallback when there's no current frame: the most-recently-updated project (matching
    # adapters/external/substrate.list_projects). Never hard-fails — a sentinel if projects is empty.
    rows = _HostReader().query("SELECT id, name FROM projects ORDER BY updated_at DESC LIMIT 1")
    if rows:
        return {"id": rows[0]["id"], "name": rows[0].get("name") or rows[0]["id"]}
    return {"id": "current", "name": "current"}


def _current_project():
    # The DETERMINISTIC current project — the current frame's project (CWD-derived); recency fallback
    # when the frame can't be resolved (off a repl CWD or a legacy schema), so this never hard-fails.
    _root, pid = _current_frame()
    return _project_named(pid) if pid else _recency_project()


def _current_project_id():
    return _current_project()["id"]


def audit_project():
    """Trust verdict for every computation in the current CS project: which cells consume a stale
    input (stale_input) or merge two live versions of one artifact (version_mix). Reuses core.audit."""
    g = _reader().read_project_graph(_current_project_id())
    verdicts = audit_graph(g)
    label = {n.id: n.label for n in g.nodes}
    flagged = flagged_verdicts(verdicts, label)   # the one flag serializer, shared with review_kit
    return {"project": g.cs_project_id, "cells": len(g.nodes),
            "clean": sum(1 for v in verdicts.values() if v.level == CLEAN), "flagged": flagged}


def audit_input_lineage(input_files, planned_output=None):
    """Pre-write check before you write ``planned_output`` from these input files. Flags two things the
    filenames alone don't reveal: (1) MERGING these inputs would collide on divergent versions of one
    artifact (a version_mix in their combined lineage), and (2) the inputs already REST on flagged data —
    a stale or mixed cell in their FOUNDATION (ancestry), e.g. an input that is the current version of its
    artifact but was itself built from a since-superseded source. Backed by core.audit."""
    if isinstance(input_files, str):
        input_files = [input_files]
    reader = _reader()
    pid = _current_project_id()
    g = reader.read_project_graph(pid)
    latest = {a.filename: a.artifact_version_id for n in g.nodes for a in n.output_surface if a.is_latest}
    vids, missing = [], []
    for f in input_files:
        if f in latest:
            vids.append(latest[f])
        else:
            missing.append(f)
    if not vids:
        return {"planned_output": planned_output, "missing_inputs": missing,
                "verdict": "LINEAGE_MISSING" if missing else "NOT_APPLICABLE"}
    combo = audit_inputs(g, vids)                             # mix from MERGING the inputs (hypothetical node)
    cone = reader.read_graph(pid, seeds=vids)                 # the inputs' own FOUNDATION (their ancestry)
    foundation = flagged_verdicts(audit_graph(cone), {n.id: n.label for n in cone.nodes})
    mix = combo.level == VERSION_MIX or any(f["verdict"] == VERSION_MIX for f in foundation)
    stale = bool(combo.stale) or any(f["verdict"] == STALE_INPUT for f in foundation)
    return {"planned_output": planned_output, "missing_inputs": missing,
            "verdict": VERSION_MIX if mix else STALE_INPUT if stale else CLEAN,
            "mixed": issues(combo.mixed), "stale": issues(combo.stale), "foundation": foundation}


def _resolve_cone(reader, proj, refs):
    """Resolve seed refs (a filename / id, or a list) to a lineage cone. Returns (normalised_refs,
    graph); graph is None when nothing resolved, so the caller formats its own *_unresolved status."""
    refs = sorted(refs) if isinstance(refs, (set, frozenset)) else refs  # JSON-safe + stable
    seeds = reader.resolve_seeds(proj["id"], refs)
    if not seeds:
        return refs, None
    return refs, reader.read_graph(proj["id"], seeds=seeds)


def render_cockpit(focus=None):
    """Write cockpit.html: the interactive cockpit for the current CS project as a static snapshot
    (inspector, rail, trace, isolate, flow toggle, framed DAG). The two big STATIC pieces — the cytoscape
    bundle and the app JS (CSS folded in) — are SAVED-ONCE artifacts referenced by their ARTIFACT MARKER
    ({{artifact:VID}} -> served URL, the only <script src> CS's HTTP viewer resolves), so each render
    writes only a KB shell + the baked graph, not the ~770KB whole. First call saves any not-yet-saved
    asset; follow the returned `next`, then re-run to publish cockpit.html.

    ``focus`` (a filename / artifact id / version id, or a list of them) renders only that seed's
    UPSTREAM lineage cone — everything it transitively depends on — instead of the whole project; the
    verdicts stay certain (a newer version off the cone is still flagged stale). Omit it for the full
    project. A filename seeds ALL of that artifact's versions."""
    import json
    import re
    proj = _current_project()
    reader = _reader()
    if focus is not None:  # None = full project; an EMPTY focus ([]/"") is a request that matched nothing
        focus, g = _resolve_cone(reader, proj, focus)
        if g is None:
            return {"project": proj["id"], "status": "focus_unresolved", "focus": focus,
                    "next": "focus matched no artifact/version in this project — check the name"}
    else:
        g = reader.read_graph(proj["id"])
    scope = {"focus": focus}   # uniform shape: focus is None for a full-project render
    # each big static asset must be a SAVED artifact so we can reference it by marker ({{artifact:VID}} is
    # the only <script src> CS's viewer resolves). Look each up by filename; write + collect any not yet
    # saved, and hand them ALL to the agent to save_artifacts once, then re-run.
    assets = [("cytoscape-dagre.bundle.min.js", "__BUNDLE_SRC__"),
              ("cockpit-app.js", "__APP_JS_SRC__")]
    subs, unsaved = {}, []
    reader = _HostReader()   # stateless; one instance for the whole asset loop
    for filename, placeholder in assets:
        rows = reader.query(
            "SELECT v.id FROM artifact_versions v JOIN artifacts a ON a.id = v.artifact_id "
            "WHERE a.filename = '" + _esc(filename) + "' ORDER BY v.version_number DESC")
        if rows:
            subs[placeholder] = host.artifact_marker(rows[0]["id"])   # {{artifact:VID}} -> served URL
        else:
            b = host.skills.read("__SKILL_NAME__", filename)
            with open(filename, "w", encoding="utf-8") as f:
                f.write(b["content"] if isinstance(b, dict) else b)
            unsaved.append(filename)
    if unsaved:
        call = "render_cockpit(focus=" + json.dumps(focus) + ")" if focus else "render_cockpit()"
        return {"project": g.cs_project_id, "nodes": len(g.nodes), "scope": scope,
                "status": "need_assets_saved", "files": unsaved,
                "next": "save_artifacts(" + json.dumps(unsaved) + ") once, then call " + call + " again"}
    t = host.skills.read("__SKILL_NAME__", "cockpit_template.html")
    t = t["content"] if isinstance(t, dict) else t
    # the getGraph wire shape from the ONE shared serializer (core.audit.graph_response, inlined) —
    # identical to what adapters/external/server.py serves, so the two surfaces can't drift.
    resp = graph_response(g)
    # escape EVERY '<' as \\u003c (valid JSON; '<' only appears inside string values). The baked GRAPH is
    # a live JS object literal in an inline <script>; a '</'-only seal misses the <!--<script> double-escape.
    # chr(92) is a backslash, on purpose: this line is inside the KERNEL_IMPL string, so writing the escape
    # literally would collapse to a bare '<' in the generated kernel and make seal a no-op.
    seal = lambda s: s.replace("<", chr(92) + "u003c")
    subs["__GRAPH__"] = seal(json.dumps(resp))
    subs["__PROJECT__"] = seal(json.dumps({"id": proj["id"], "name": proj["name"]}))
    # ONE pass: inserted values are never re-scanned, so a placeholder token in a baked value can't
    # corrupt another slot.
    html = re.sub("|".join(re.escape(k) for k in subs), lambda m: subs[m.group(0)], t)
    with open("cockpit.html", "w", encoding="utf-8") as f:
        f.write(html)
    return {"project": g.cs_project_id, "nodes": len(g.nodes), "scope": scope, "status": "rendered",
            "files": ["cockpit.html"], "next": "call save_artifacts(['cockpit.html']) to publish"}


def review_subgraph(nodes, scope="upstream"):
    """A deterministic review brief over a chosen lineage subgraph. Resolve ``nodes`` (a filename /
    artifact id / version id, or a list of them) to seeds, read their UPSTREAM cone, and hand back the
    evidence — flagged conflicts, lineage edges, artifact inventory, and the cell ids to fetch code
    for. Fixes WHAT to look at (same nodes -> same brief); the agent reasons and may walk deeper.
    Backed by core.review_kit; ``scope`` is a passthrough label for now (upstream cone)."""
    proj = _current_project()
    reader = _reader()
    nodes, g = _resolve_cone(reader, proj, nodes)
    if g is None:
        return {"project": proj["id"], "status": "seeds_unresolved", "nodes": nodes,
                "next": "none matched an artifact/version in this project; check the names/ids"}
    return review_kit(g, scope)


def review_chat(scope="upstream"):
    """A deterministic review brief scoped to THIS conversation: seed it with the artifacts this chat
    produced, then read their UPSTREAM cone and hand back the evidence (flagged conflicts, lineage,
    inventory, cells). The seeds are just this chat's outputs, but the cone + audit reach versions made
    in OTHER conversations — so a divergent version created elsewhere still surfaces as a conflict here.
    No args: it resolves the current chat itself. Use review_subgraph(<names>) to scope by hand instead.
    Backed by core.review_kit; ``scope`` is a passthrough label for now (upstream cone)."""
    root, pid = _current_frame()   # resolve the current conversation ONCE (root + project id together)
    proj = _project_named(pid) if pid else _recency_project()
    if not root:
        return {"project": proj["id"], "status": "no_current_chat",
                "next": "couldn't resolve the current conversation; use review_subgraph(<names>) to scope explicitly"}
    reader = _reader()
    seeds = reader.chat_seeds(proj["id"], root)
    if not seeds:
        return {"project": proj["id"], "status": "chat_empty", "root_frame": root,
                "next": "this conversation has produced no artifacts yet — nothing to review"}
    kit = review_kit(reader.read_graph(proj["id"], seeds=seeds), scope)
    kit["chat_scoped"] = True
    return kit


def _trusted_inputs(graph, keep):
    # the DIRECT dependencies crossing INTO the selection from EXCLUDED producers — the human's trust
    # cut. Not drawn in the induced brief, but the agent should know what the selection rests on. Deduped
    # by via-version (one row per distinct external version — two picked nodes sharing an input collapse
    # to one), so a version_mix's TWO live versions still both appear. Reuses review_kit's _via_fields.
    ref_of = _ref_map(graph)
    label = {n.id: n.label for n in graph.nodes}
    seen, out = set(), []
    for e in graph.edges:
        v = e.via_artifact_version_id
        if e.dst_node_id in keep and e.src_node_id not in keep and v not in seen:
            seen.add(v)
            row = _via_fields(ref_of.get(v), v)
            row["from"] = label.get(e.src_node_id, e.src_node_id)
            out.append(row)
    out.sort(key=lambda r: (r["from"], r["artifact"], r["version"] or 0))
    return out


def review_selection(nodes, scope="selection"):
    """A deterministic review brief over EXACTLY the picked nodes — a hand-selected region from the
    cockpit. The STRUCTURE is INDUCED down to just those nodes and the edges among them (no full
    lineage cone dumped on the agent), but the VERDICTS are taken from the FULL-project audit, so the
    flags on the shown nodes match exactly what the cockpit colours them. That lets you hand the agent
    a suspicious fork, cut the trusted trunk out of the picture, and still have every stale_input /
    version_mix reflect the whole graph — a conflict can rest on inputs that aren't drawn here, so
    ``trusted_inputs`` lists what the selection consumes from outside itself. Resolve ``nodes``
    (version ids, or filenames / artifact ids) to the selection. Backed by core.induced_subgraph +
    core.review_kit."""
    proj = _current_project()
    reader = _reader()
    selected = reader.resolve_seeds(proj["id"], nodes)   # -> version ids (self-artifacts dropped)
    if not selected:
        return {"project": proj["id"], "status": "seeds_unresolved", "nodes": nodes,
                "next": "none matched an artifact/version in this project; check the names/ids"}
    full = reader.read_project_graph(proj["id"])   # the AUTHORITATIVE graph — same basis as the cockpit
    producer_of = _producer_map(full)              # version id -> its producing node (shared with audit)
    keep = {producer_of[v] for v in selected if v in producer_of}
    if not keep:   # versions resolved but none has a producing node in the graph — an inconsistent
        return {"project": proj["id"], "status": "seeds_no_producers", "nodes": nodes,   # DB, not an
                "next": "the selected versions have no producing cell in this project (unexpected — the graph may be inconsistent)"}
    kit = review_kit(induced_subgraph(full, keep), scope, verdicts=audit_graph(full))
    kit["basis"] = "full_project_audit"
    kit["trusted_inputs"] = _trusted_inputs(full, keep)
    kit["note"] = ("induced subgraph — only the selected nodes and the edges among them are shown, "
                   "NOT a full lineage cone. The flags come from the FULL-project audit, so a flag can "
                   "name an artifact that isn't drawn here — each flag's mixed/stale field names the "
                   "exact artifact + versions behind it. 'boundary' lists only sources INSIDE the "
                   "selection (empty when you cut the trunk); 'trusted_inputs' is the real upstream "
                   "boundary — the direct dependencies the selection consumes from the excluded region.")
    return kit
'''


