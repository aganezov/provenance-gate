# ---- the in-CS cockpit template, generated from ui/cockpit.html at build time ----
# The full cockpit fetches getProjects()/getGraph() from /api/*; in-CS there's no server. So we (1) swap
# the inlined ~670KB vendor for a __BUNDLE_SRC__ marker <script src> (render_cockpit fills the artifact
# marker), and (2) inject a PG:SKILL-DATA block — the same getProjects/getGraph override shape build_mock's
# PG:MOCK uses for Claude Design — that serves ONE baked project graph from __GRAPH__/__PROJECT__.
SKILL_DATA_BLOCK = '''
// ===== PG:SKILL-DATA — generated by build_skill.py; do not edit. In-CS there is no server, so
// getProjects()/getGraph() serve the ONE project graph render_cockpit baked in. getGraph serves once
// then returns null ("unchanged"), so the cockpit's 2s poll won't re-layout and reset the view. =====
(function () {
  const PROJECT = __PROJECT__;   // {id, name}
  const GRAPH = __GRAPH__;       // core.audit.graph_response shape (asdict(graph) + per-node verdict)
  window.__PG_GRAPH = GRAPH;     // exposed for the no-bundle fallback below
  window.__PG_SNAPSHOT = true;   // this render is a STATIC snapshot: cockpit shows the bake date, no live poll
  let served = false;
  // these replace the PG:LOG-wrapped getProjects/getGraph — telemetry is a deliberate no-op in CS (no /api/log)
  getProjects = async () => [PROJECT];
  getGraph = async (pid) => {
    if (pid !== PROJECT.id || served) return null;   // static baked graph: nothing changes after serve #1
    served = true;
    return GRAPH;
  };
})();
// ===== /PG:SKILL-DATA =====
'''

