#!/usr/bin/env python3
"""build_skill.py — bundle the pure core + cs_skill reader + an in-CS kernel into ONE self-contained
``kernel.py`` (stdlib-only, zero intra-package imports) plus a ``SKILL.md``, and package/install it
as a Claude Science skill.

Inlining (vs vendoring ``core`` as a package) keeps the skill a two-file artifact and sidesteps the
"can a kernel import a package" question — a CS skill kernel is a single auto-loaded module. Our
stdlib-only core makes inlining a clean strip-imports-and-concatenate.

Modes (default = build):
  build       regenerate design/skill_dist/{kernel.py, SKILL.md, + assets}
  --zip       also write design/skill_dist/provenance-gate.zip  (for "Upload a skill" — one-time register)
  --share     stage kernel.py + SKILL.md + cockpit_template.html into ~/pg-share for the in-CS resync
  --watch     rebuild + --share on every save under src/ or design/ (the local auto-refresh loop)
  --name X    build/deploy under skill name X (default provenance-gate) — use a throwaway to validate first

Loop: `--zip` -> upload once to register -> then run `--watch` locally; each save rebuilds + stages to
~/pg-share, and in CS you run `pg_dev_sync()` (host.skills.edit from ~/pg-share) + a fresh conversation.
Uploaded skills aren't on disk and the kernel has no __file__ (both probed), so there is no symlink path.
"""

